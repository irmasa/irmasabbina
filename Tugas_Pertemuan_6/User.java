package Tugas_Pertemuan_6;
public class User {
private int nim;
private String nama;
private double ipk;

public void Setnim (int nim){
    this.nim = nim;
}
public void Setnama(String nama){
    this.nama = nama;
}
public void Setipk(double ipk){
    this.ipk = ipk;
}
public int Getnim(){
    return this.nim;
}
public String Getnama(){
    return this.nama;
}
public double Getipk(){
    return this.ipk;
}
}
